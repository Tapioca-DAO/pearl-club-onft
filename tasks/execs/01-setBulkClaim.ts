import { HardhatRuntimeEnvironment } from 'hardhat/types';
import inquirer from 'inquirer';

export const setBulkClaim__task = async (
    {},
    hre: HardhatRuntimeEnvironment,
) => {
    const { ethers } = hre;

    const tag = await hre.SDK.hardhatUtils.askForTag(hre, 'local');
    const pearlClubONFT = await ethers.getContractAt(
        'PearlClubONFT',
        (
            await hre.SDK.hardhatUtils.getLocalContract(
                hre,
                'PearlClubONFT',
                tag,
            )
        ).deployment.address,
    );

    const { finalize } = await inquirer.prompt({
        type: 'confirm',
        message: 'Finalize claim?',
        name: 'finalize',
    });

    const addresses = getAddresses();

    for (let i = 700; i < addresses.length; i = i + 50) {
        console.log(`Setting claim for ${i} to ${i + 50}`);
        const slice = addresses.slice(i, i + 50);
        (await pearlClubONFT.setClaimAvailable(slice, finalize)).wait(3);
    }

    (await pearlClubONFT.setClaimAvailable([], finalize)).wait(3);

    console.log('[+] Claim set successfully');
};
